# @module <stringx.rb>
class Stringx
	# => It turns the first letter
	# => of a string into an upper case.
	def capitalize(str)
		return str.capitalize
	end

	# => They turn into a downcase
	# => or into an uppercase
	def downcase(str)
		return str.downcase
	end
	def upcase(str)
		return str.upcase
	end
	# => It turns the string to
	# => the reverse side
	def reverse(str)
		return str.reverse
	end

	# => It does not modify the
	# => original string.
	def join(strOne, strTwo)
		return strOne + strTwo
	end
	
end